package com.cas.das.core.service.ext;

/**
 * 监视器事件
 * 
 * @author NiuLei
 */
public class DirectoryMonitorEvent {

	/**
	 * 创建事件名
	 */
	public static final String CREATE_EVENT_NAME = "ENTRY_CREATE";

	/**
	 * 删除事件名
	 */
	public static final String DELETE_EVENT_NAME = "ENTRY_DELETE";

	/**
	 * 修改事件名
	 */
	public static final String MODIFY_EVENT_NAME = "ENTRY_MODIFY";

	/**
	 * 事件名
	 */
	private String eventName;

	/**
	 * 文件路径
	 */
	private String filePath;

	/**
	 * 构造
	 */
	DirectoryMonitorEvent() {
	}

	public String getEventName() {
		return eventName;
	}

	void setEventName(String eventName) {
		this.eventName = eventName;
	}

	public String getFilePath() {
		return filePath;
	}

	void setFilePath(String filePath) {
		this.filePath = filePath;
	}

}
